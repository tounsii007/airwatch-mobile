//
// Powers locale-aware country search: typing "Tunesien" (de) / "Tunisie" (fr) /
// "Tunezja" (pl) / "تونس" (ar) all resolve to the canonical English "Tunisia"
// so downstream code that keys off country names (search, airport filtering,
// flag lookup) keeps working without every layer needing to know about
// translations.
//
// Lazily seeded the first time a reverse-lookup helper fires — building the
// map at app boot would be wasted work for users that never search.

Map<String, String>? _reverseCountryIndex;

/// Strip diacritics + lowercase + collapse whitespace so "tunesien", "Tunesien"
/// and "  TUNESIEN  " all resolve identically. Mirrors the normalisation used
/// in the web app's country-translations.ts so query handling stays consistent
/// across the two surfaces.
String _normalizeCountryQuery(String s) {
  // ASCII fast-path keeps the hot call site (every keystroke during search)
  // allocation-free for the common case of plain English input.
  if (s.codeUnits.every((c) => c < 0x80)) {
    return s.trim().toLowerCase();
  }
  const diacritics = {
    'à': 'a',
    'á': 'a',
    'â': 'a',
    'ã': 'a',
    'ä': 'a',
    'å': 'a',
    'æ': 'ae',
    'ç': 'c',
    'è': 'e',
    'é': 'e',
    'ê': 'e',
    'ë': 'e',
    'ì': 'i',
    'í': 'i',
    'î': 'i',
    'ï': 'i',
    'ñ': 'n',
    'ò': 'o',
    'ó': 'o',
    'ô': 'o',
    'õ': 'o',
    'ö': 'o',
    'ø': 'o',
    'œ': 'oe',
    'ù': 'u',
    'ú': 'u',
    'û': 'u',
    'ü': 'u',
    'ý': 'y',
    'ÿ': 'y',
    'ß': 'ss',
  };
  final lower = s.trim().toLowerCase();
  final buf = StringBuffer();
  for (final ch in lower.split('')) {
    buf.write(diacritics[ch] ?? ch);
  }
  return buf.toString();
}

void _seedCountryReverseIndex() {
  if (_reverseCountryIndex != null) return;
  final map = <String, String>{};
  for (final entry in countryTranslations.entries) {
    final canonical = entry.key;
    // Index the canonical English name itself so "Germany" / "germany" /
    // "GERMANY" all resolve to "Germany" (handles case-only variants
    // without forcing every caller to call .toLowerCase first).
    map[_normalizeCountryQuery(canonical)] = canonical;
    // Plus every translation across all eight non-English locales.
    for (final loc in entry.value.values) {
      map[_normalizeCountryQuery(loc)] = canonical;
    }
  }
  _reverseCountryIndex = map;
}

/// Reverse lookup — given a free-form query in any of the nine supported
/// locales, return the canonical English country name, or `null` if nothing
/// matches.
///
/// Examples:
/// * `resolveCountryAlias("Tunesien")` → `"Tunisia"` (de)
/// * `resolveCountryAlias("Tunisie")`  → `"Tunisia"` (fr)
/// * `resolveCountryAlias("Tunezja")`  → `"Tunisia"` (pl)
/// * `resolveCountryAlias("تونس")`     → `"Tunisia"` (ar)
/// * `resolveCountryAlias("Allemagne")` → `"Germany"` (fr)
/// * `resolveCountryAlias("Almanya")`   → `"Germany"` (tr)
/// * `resolveCountryAlias("Niemcy")`    → `"Germany"` (pl)
/// * `resolveCountryAlias("xyz")` → `null`
String? resolveCountryAlias(String query) {
  if (query.isEmpty) return null;
  _seedCountryReverseIndex();
  return _reverseCountryIndex![_normalizeCountryQuery(query)];
}

/// Substring-match helper — true if `query` appears (locale-insensitive)
/// inside the English name OR any of the eight non-English translations of
/// `countryEnglishName`.
///
/// Used by aircraft / airport search filters so typing "Marok" in a German
/// UI matches "Morocco" (de: Marokko) without needing to switch locales
/// mid-search. Likewise "Marrue" (es), "Maroko" (pl), "Fas" (tr), "المغرب" (ar)
/// all match the same canonical Morocco.
bool countryNameMatches(String countryEnglishName, String query) {
  final q = _normalizeCountryQuery(query);
  if (q.isEmpty) return false;
  if (_normalizeCountryQuery(countryEnglishName).contains(q)) return true;
  final translations = countryTranslations[countryEnglishName];
  if (translations == null) return false;
  for (final localized in translations.values) {
    if (_normalizeCountryQuery(localized).contains(q)) return true;
  }
  return false;
}
