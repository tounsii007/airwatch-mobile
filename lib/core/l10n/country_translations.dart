// Multi-locale country index with bidirectional lookup.
//
// Two lookup directions:
//   * Forward (localizeCountry) — canonical English name + locale → variant
//     for display. "Tunisia" + "de" → "Tunesien".
//   * Reverse (resolveCountryAlias, countryNameMatches) — free-form query in
//     any of the nine supported locales → canonical English. Powers "typing
//     Tunesien finds Tunisair / Tunis airports / flights to TUN even when the
//     app language is English / French / Arabic / …".
//
// All nine app locales are first-class: en / de / fr / es / it / ar / pl / nl
// / tr. Every entry carries the English canonical form; other locales fall
// back to English when no translation is supplied.
//
// The reverse index is keyed on a diacritic-stripped, lower-cased form of
// every known alias. "tunesien", "TUNESIEN", " Tunesien ", "Tünesien" all
// collapse to the same key. Mirrors the web app's approach in
// `country-translations.ts` so the two surfaces stay in lockstep.
//
// Some countries have several common English forms: `USA` / `United States`,
// `UK` / `United Kingdom`, `Czechia` / `Czech Republic`. They are listed as
// separate canonical entries but resolve to the same ISO-2 code via
// `CountryDatabase`, so airline / airport filtering keeps working regardless
// of which form upstream sends.

